public class OperatorDemo {
    /*
     * Arithmatic + - / * %
     * 
     * 
     */

    public static void main(String args[]) {
        int m = 6, n = 4;
        int r1 = m + n;
        int r2 = m - n;
        int r3 = m * n;
        int r4 = m / n;
        // double r4 = (double)m/n;
        int r5 = m % n;

        /*
         * int m=4;
         * int n=3;
         * n++; Pre increament
         * //n+=1; //n=n+1; // Same
         * ++n; //Post increament
         */

        System.out.println(r1);
        System.out.println(r2);
        System.out.println(r3);
        System.out.println(r4);
        System.out.println(r5);
    }

}
