public class ArrayDemo {
    public static void main(String[] args) {
        int nums[] = new int[4];

        nums[0] = 23;
        nums[1] = 45;
        nums[2] = 9;
        nums[3] = 60;

        for (int i = 0; i < 4; i++) {
            System.out.println(nums[i]);
        }
    }
}
